// TO LOWER CASE
// https://leetcode.com/problems/to-lower-case/


package com.akshat;

public class to_lower_case {

    public static void main(String[] args) {

        String s = "Hello";

        System.out.println(toLowerCase(s));

    }

    static String toLowerCase(String s) {
        s=s.toLowerCase();
        return s;
    }

}
